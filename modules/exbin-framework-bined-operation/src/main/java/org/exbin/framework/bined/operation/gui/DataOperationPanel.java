    public void setDataMethods(List<? extends DataOperationMethod> dataMethods) {
        DefaultListModel<DataOperationMethod> listModel = (DefaultListModel<DataOperationMethod>) optionsList.getModel();
        for (DataOperationMethod dataMethod : dataMethods) {
            listModel.addElement(dataMethod);
        }
    }

    @Nonnull
    public Optional<DataOperationMethod> getActiveMethod() {
        return Optional.ofNullable(activeMethod);
    }

    @Nonnull
    public Optional<Component> getActiveComponent() {
        return Optional.ofNullable(activeComponent);
    }

    public void setPreviewComponent(Component previewComponent) {
        if (this.previewComponent != null) {
            previewPanel.remove(this.previewComponent);
        }

        this.previewComponent = previewComponent;
        previewPanel.add(previewComponent);
        previewPanel.revalidate();
        previewPanel.repaint();
    }

