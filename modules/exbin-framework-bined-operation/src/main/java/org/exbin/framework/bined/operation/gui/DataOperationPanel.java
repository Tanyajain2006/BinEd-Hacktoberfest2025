    public void selectActiveMethod(@Nullable DataOperationMethod method) {
        DefaultListModel<DataOperationMethod> listModel = (DefaultListModel<DataOperationMethod>) optionsList.getModel();
        if (method == null && !listModel.isEmpty()) {
            optionsList.setSelectedIndex(0);
        } else {
            int methodIndex = listModel.indexOf(method);
            if (methodIndex >= 0) {
                optionsList.setSelectedIndex(methodIndex);
            }
        }
    }

